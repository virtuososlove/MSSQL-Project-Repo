PRINT '[NT SERVICE\MSSQL$MSSQLSERVER01] login''i MSSQLSERVER02 üzerinde oluþturuldu.';
GO

GRANT CONNECT ON ENDPOINT::Mirroring_Endpoint_MSSQLSERVER02 TO [NT SERVICE\MSSQL$MSSQLSERVER01];
PRINT '[NT SERVICE\MSSQL$MSSQLSERVER01] login''ine Mirroring_Endpoint_MSSQLSERVER02 için CONNECT izni verildi.';
GO

