    DROP LOGIN [NT SERVICE\MSSQL$MSSQLSERVER01];
    PRINT 'Mevcut [NT SERVICE\MSSQL$MSSQLSERVER01] login''i MSSQLSERVER02 üzerinden silindi.';
END
GO

